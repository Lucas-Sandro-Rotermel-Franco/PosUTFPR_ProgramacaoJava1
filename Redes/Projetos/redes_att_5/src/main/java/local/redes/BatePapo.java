    private class ReceiverThread extends Thread {
        @Override
        public void run() {
            try {
                byte[] buf = new byte[128];
                while (true) {
                    DatagramPacket packet = new DatagramPacket(buf, buf.length);
                    socketRecv.receive(packet);
                    final String mensagem = new String(packet.getData(), 0, packet.getLength());

                    if (usuario != null && !mensagem.contains(usuario) && !mensagem.toLowerCase().contains("sair")) {
                        SwingUtilities.invokeLater(new Runnable() {
                            public void run() {
                                TaTexto.append(mensagem + "\n");
                            }
                        });
                    }

                    buf = new byte[128];
                }
            } catch (IOException ex) {
                ex.printStackTrace();
            }
        }
    }
