    private void BtConectarActionPerformed(java.awt.event.ActionEvent evt) {
        try {
            usuario = TfUsuario.getText();
            if (usuario == null || usuario.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Informe o nome de usuário antes de conectar.", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }

            endereco = InetAddress.getByName("224.1.1.20");
            porta = 50000;
            TfEndereco.setText(endereco.getHostAddress());
            TfPorta.setText(String.valueOf(porta));

            socketRecv = new MulticastSocket(porta);
            try {
                NetworkInterface nif = NetworkInterface.getByInetAddress(InetAddress.getLocalHost());
                socketRecv.joinGroup(new InetSocketAddress(endereco, porta), nif);
            } catch (Exception ex) {
                try {
                    socketRecv.joinGroup(endereco);
                } catch (Exception ex2) {
                    // ignore
                }
            }

            socketSend = new MulticastSocket();
            try {
                NetworkInterface nif2 = NetworkInterface.getByInetAddress(InetAddress.getLocalHost());
                socketSend.joinGroup(new InetSocketAddress(endereco, porta), nif2);
            } catch (Exception ex) {
                try {
                    socketSend.joinGroup(endereco);
                } catch (Exception ex2) {
                    // ignore
                }
            }

            receiverThread = new ReceiverThread();
            receiverThread.start();

            BtConectar.setEnabled(false);
            TfUsuario.setEnabled(false);
            TfEndereco.setEnabled(false);
            TfPorta.setEnabled(false);

            TaTexto.append("Conectado em " + endereco.getHostAddress() + ":" + porta + "\n");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao conectar: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }

    private void BtEnviarActionPerformed(java.awt.event.ActionEvent evt) {
        try {
            if (socketSend == null) {
                JOptionPane.showMessageDialog(this, "Você precisa conectar antes de enviar mensagens.", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }

            String texto = TfTexto.getText();
            if (texto == null || texto.trim().isEmpty()) {
                return;
            }

            String mensagem = usuario + " diz: " + texto;
            byte[] msg = mensagem.getBytes();

            DatagramPacket dgPacket = new DatagramPacket(msg, msg.length, endereco, porta);
            socketSend.send(dgPacket);

            if (texto.equalsIgnoreCase("sair") || texto.equalsIgnoreCase("Sair")) {
                System.exit(0);
            }

            TfTexto.setText("");

        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Erro ao enviar: " + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }

